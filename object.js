//create an object and add three properties and values to it using dot notation



var person = {};             //creating object named person

person.name = 'John';        //using dot notation

person.age = 15;

person.height = '6\'5';



//create an object and add properties and values to it using bracket notation



var furniture = {};

furniture['material'] = 'wood';

furniture['numberOfFurniture'] = 5;

furniture['room type'] = 'living room';   /* the property 'room type' can
                                            have space between words while
                                            using bracket notation, which is
                                            not possible while using dot notation */

furniture['typeof'] = 'chair';            /* the keyword 'typeof' can be used
                                             as a variable name in bracket notation */




