
//JAVASCRIPT FUNCTION SYNTAX AND FUNCTION CALL


function goodMorning(name) {
return 'Good Morning, ' + name + '. How are you today?';
}

goodMorning('Jimmy');          
//The function goodMorning, when invoked or called with the argument 'Jimmy', returns "Good Morning, Jimmy. How are you today?"



function goodEvening(name,task) {
    return 'Good evening, ' + name + '. Thanks for ' + task + '.' ;
}

goodEvening('Mary','watering the plants');
//The function goodEvening, when called with the arguments in the brackets, returns"Good evening, Mary. Thanks for watering the plants."